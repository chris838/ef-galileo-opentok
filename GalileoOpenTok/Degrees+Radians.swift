
import Foundation

extension Int {
    var degreesToRadians : Double {
        return Double(self) * Double(M_PI) / 180.0
    }
}

extension CGFloat {
    var degreesToRadians : Double {
        return Double(self) * Double(M_PI) / 180.0
    }
}

extension Double {
    var degreesToRadians : Double {
        return Double(self) * Double(M_PI) / 180.0
    }
}